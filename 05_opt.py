# 산술연산자
# (+ - * / 목(//) 나머지(%) 거듭제곱(**))
print(3 + 5) # 8
print(10 - 4) # 6
print(4 * 5) # 20
print(20 / 4) # 5.0
print(7 // 3) # 2 (7/3 = 2와2분의1)이므로 //는 몫이기 때문에 2
print(7 % 3) # 1 (7/3 = 2와2분의1)이므로 %는 나머지기 때문에 1
print(2 ** 5) # 32 (2의 5제곱)

# ===============================================================

# 연산 우선순위와 우선순위 지정

# ** > *, /, //, % > +, - 순서로 우선순위가 정해진다.
print(2 + 8 * 3) # 8 * 3 연산후 2와 더한다
print((2 + 8) * 3) # 괄호 안의 연산을 먼저 계산한후 * 3 을 계산한다.

