# 복합연산자

result = 3 * 5
print(result) # 15
# += : 기존 값에서 오른쪽 값을 더한 뒤 재할당 
# -= : 기존 값에서 오른쪽 값을 뺸 뒤 재할당 
# *= : 기존 값에서 오른쪽 값을 곱한 뒤 재할당 
# /= : 기존 값에서 오른쪽 값을 나눈 뒤 재할당 

result += 10 # 25
print(result)
result -= 5 # 20
print(result)
result *= 3 # 60
print(result)
result /= 2 # 30.0
print(result)

